// example showing the concept of 2d array
class Demo14{
    public static void main(String args[]){
        int arr[][] = {
            {1,2,3},
            {3,4,5},
            {7,8,8}
        };
        System.out.print("\tarr[0][0] : "+arr[0][0]);
        System.out.print("\tarr[0][1] : "+arr[0][1]);
        System.out.println("\tarr[0][2] : "+arr[0][2]);
        System.out.print("\tarr[1][0] : "+arr[1][0]);
        System.out.print("\tarr[1][1] : "+arr[1][1]);
        System.out.println("\tarr[1][2] : "+arr[1][2]);
        System.out.print("\tarr[2][0] : "+arr[2][0]);
        System.out.print("\tarr[2][1] : "+arr[2][1]);
        System.out.print("\tarr[2][2] : "+arr[2][2]);
    }
}